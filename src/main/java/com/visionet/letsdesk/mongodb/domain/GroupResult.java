package com.visionet.letsdesk.mongodb.domain;

public class GroupResult {
	
	private Long count;
	private Long sum;
	private Long max;
	private Long min;
	private Long avg;
	private Long self;
	
	public Long getCount() {
		return count;
	}
	public void setCount(Long count) {
		this.count = count;
	}
	public Long getSum() {
		return sum;
	}
	public void setSum(Long sum) {
		this.sum = sum;
	}
	public Long getMax() {
		return max;
	}
	public void setMax(Long max) {
		this.max = max;
	}
	public Long getMin() {
		return min;
	}
	public void setMin(Long min) {
		this.min = min;
	}
	public Long getAvg() {
		return avg;
	}
	public void setAvg(Long avg) {
		this.avg = avg;
	}
	public Long getSelf() {
		return self;
	}
	public void setSelf(Long self) {
		this.self = self;
	}
	
	
	

}
